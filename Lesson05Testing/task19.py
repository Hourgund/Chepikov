# 1) not
# not True --> False
# not False --> True
print(not True)
print(not False)

# 2) and
# True and True --> True
# True and False --> False
# True and True and False --> False
# False and False --> False
print(True and True)
print(True and False)
print(False and False)

# 3) or
# True or True --> True
# True or False --> True
# False or False --> False
print(True or True)
print(True or False)
print(False or False)

